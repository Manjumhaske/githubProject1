package javapractice;

public class Calculatefactorialofanumber {

	
	public static void main(StringBuilder[] args) {
		int a = 5;
		int result=1;       //   i=5 
		for(int i=a;i>=1;i--)  // i-- = 4*3*2*1 = 24*5=120
			result = result*i;    
		System.out.println(result);
		
		
	}

	
	
	
	
	
}
